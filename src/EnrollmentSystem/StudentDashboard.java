    public void updateProfile(){
        try {
            Connection con = DBConnection.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Students WHERE Student_ID = " + jTextFieldStudNo.getText() + ";");
            if (rs.next()) {
                jLabelStudNo.setText(rs.getString("Student_ID"));
                jLabelFName.setText(rs.getString("First_Name"));
                jLabelMName.setText(rs.getString("Middle_Name"));
                jLabelSName.setText(rs.getString("Surname"));
                jLabelBdate.setText(rs.getString("Birth_Date"));
                jLabelAge.setText(rs.getString("Age"));
                jLabelCourse.setText(rs.getString("Course_ID"));
                jLabelYearLvl.setText(rs.getString("Year_Level"));
                jLabelSection.setText(rs.getString("Section"));
                jLabelAddress.setText(rs.getString("Address"));
                jLabelPhoneNo.setText(rs.getString("Phone_No"));
                jLabelGender.setText(rs.getString("Gender"));
            } else {
                JOptionPane.showMessageDialog(null, "Student not found.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex, "Error fetching data.", JOptionPane.ERROR_MESSAGE);
        }
    }
    
