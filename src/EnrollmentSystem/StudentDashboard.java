    private void jButtonAddEnrolledSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddEnrolledSubActionPerformed
        try {
            int selectedRow = jTableSubs.getSelectedRow();
            String selectedSub = jTableSubs.getModel().getValueAt(selectedRow, 0).toString();
            String studID = jLabelSN.getText();

            try {
                Connection con = DBConnection.getConnection();
                PreparedStatement ps = con.prepareStatement("SELECT * FROM Enroll WHERE Student_ID = ? AND Subject_ID = ?");
                ps.setString(1, studID);
                ps.setString(2, selectedSub);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Subject already enrolled.");
                } else {
                    int confirm = JOptionPane.showConfirmDialog(null, "Confirm Enroll?", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    if (confirm == JOptionPane.YES_NO_OPTION) {
                        theQuery("INSERT INTO Enroll(Student_ID, Subject_ID) VALUES(" + jLabelSN.getText() + ", '" + selectedSub + "')");
                        JOptionPane.showMessageDialog(null, "Enrolled to " + selectedSub + "Successfully");
                        updateTableEnrolledSubjects();
                    }
                }
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Select a subject to enroll.");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Select a subject.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAddEnrolledSubActionPerformed

