    public void updateTableSubjects() {
        //updates the subjects table
        DefaultTableModel tModel = new DefaultTableModel();
        Object[] columnsName = new Object[2];

        columnsName[0] = "Subject ID";
        columnsName[1] = "Subject Name";

        tModel.setColumnIdentifiers(columnsName);

        Object[] rowData = new Object[2];

        for (int i = 0; i < getSubjects().size(); i++) {
            rowData[0] = getSubjects().get(i).getSubID();
            rowData[1] = getSubjects().get(i).getSubName();

            tModel.addRow(rowData);
        }
        jTableSubs.setModel(tModel);
    }

    private static ArrayList<Enroll> getEnroll() {
        ArrayList<Enroll> enroll = new ArrayList<Enroll>();
        Connection con = DBConnection.getConnection();
        Enroll e;
        String studNo = jLabelSN.getText();

        try {

            PreparedStatement ps = con.prepareStatement("""
                                 SELECT Subjects.Subject_ID, Subjects.Subject_Name, Students.Student_ID
                                 FROM Subjects
                                 INNER JOIN Enroll ON Subjects.Subject_ID = Enroll.Subject_ID
                                 INNER JOIN Students ON Enroll.Student_ID = Students.Student_ID
                                 WHERE Students.Student_ID = ?;""");
            ps.setString(1, studNo);
            ResultSet rs = ps.executeQuery(); 
            while (rs.next()) {
                e = new Enroll(
                        rs.getInt("Student_ID"),
                        rs.getString("Subject_ID"),
                        rs.getString("Subject_Name")
                );
                enroll.add(e);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CourseManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        return enroll;
    }
    
    private static ArrayList<Grades> getGrades() {
        ArrayList<Grades> grades = new ArrayList<Grades>();
        Connection con = DBConnection.getConnection();
        Grades e;
        String studID = jLabelSN.getText();

        try {

            PreparedStatement ps = con.prepareStatement("SELECT * FROM Grades WHERE Student_ID = ?");
            ps.setString(1, studID);
            ResultSet rs = ps.executeQuery(); 
            while (rs.next()) {
                e = new Grades(
                        rs.getFloat("Grade"),
                        rs.getString("Subject_ID"),
                        rs.getString("Student_ID")
                );
                grades.add(e);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CourseManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        return grades;
    }

