    private void updateJComboBoxDeleteDept(JComboBox comboBox) {
        //updates all the comboboxes that is returned
        comboBox.removeAllItems();
        Connection con = DBConnection.getConnection();
        String query = "SELECT Department_ID FROM Department";
        try {
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                comboBox.addItem(rs.getString("Department_ID"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private static ArrayList<Faculty> getFaculty() {
        ArrayList<Faculty> faculty = new ArrayList<Faculty>();
        Connection con = DBConnection.getConnection();
        Faculty f;
        try {
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM Faculty");
            while (rs.next()) {
                f = new Faculty(
                        rs.getInt("Faculty_ID"),
                        rs.getString("First_Name"),
                        rs.getString("Middle_Name"),
                        rs.getString("Surname"),
                        rs.getString("Birth_Date"),
                        rs.getInt("Age"),
                        rs.getString("Address"),
                        rs.getString("Phone_No"),
                        rs.getString("Gender"),
                        rs.getString("Department_ID")
                );
                faculty.add(f);
            }

        } catch (SQLException ex) {
            Logger.getLogger(StudentEnrollment.class.getName()).log(Level.SEVERE, null, ex);
        }
        return faculty;
    }

    private void updateJTable(JTable table, DefaultTableModel tModel, Object[] columnsName, Object[] rowData) {
        //updates the table that is returned
        tModel = new DefaultTableModel();
        columnsName = new Object[6];

        columnsName[0] = "Faculty ID";
        columnsName[1] = "FName";
        columnsName[2] = "MName";
        columnsName[3] = "SName";
        columnsName[4] = "Age";
        columnsName[5] = "Department";

        tModel.setColumnIdentifiers(columnsName);

        rowData = new Object[6];

        for (int i = 0; i < getFaculty().size(); i++) {
            rowData[0] = getFaculty().get(i).getFacultyID();
            rowData[1] = getFaculty().get(i).getFName();
            rowData[2] = getFaculty().get(i).getMName();
            rowData[3] = getFaculty().get(i).getSName();
            rowData[4] = getFaculty().get(i).getAge();
            rowData[5] = getFaculty().get(i).getDeptID();

            tModel.addRow(rowData);
        }
        table.setModel(tModel);
    }

