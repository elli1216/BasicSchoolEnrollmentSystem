    private void jButtonAddDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddDeptActionPerformed
        String DeptID = jTextFieldAddDeptID.getText();
        String DeptName = jTextFieldAddDeptName.getText();
        String checkQuery = "SELECT * FROM Department WHERE Department_ID = ?";
        if (DeptID.isEmpty() || DeptName.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter all fields.");
        } else {
            try {
                Connection con = DBConnection.getConnection();
                PreparedStatement ps = con.prepareStatement(checkQuery);
                ps.setString(1, DeptID);
                ResultSet rs = ps.executeQuery();
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null, "Department already existing.", "ERROR", JOptionPane.ERROR_MESSAGE);
                } else {
                    int confirm = JOptionPane.showConfirmDialog(null,
                            "Confirm add " + DeptName + "(" + DeptID + ") to database?",
                            "CONFIRM", JOptionPane.INFORMATION_MESSAGE);
                    if (confirm == JOptionPane.YES_OPTION) {
                        theQuery("INSERT INTO Department(Department_ID, Department_Name) VALUES('" + DeptID + "', '" + DeptName + "');");
                        JOptionPane.showMessageDialog(null, "Added Successfully!");
                        jTextFieldAddDeptID.setText("");
                        jTextFieldAddDeptName.setText("");
                    }
                    updateJComboBoxDeleteDept(jComboBoxDeptID);
                    updateJComboBoxDeleteDept(jComboBoxDeptIDUD);
                    updateJComboBoxDeleteDept(jComboBoxDeleteDept);
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_jButtonAddDeptActionPerformed

