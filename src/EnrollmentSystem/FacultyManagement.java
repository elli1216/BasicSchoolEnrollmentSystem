    private void jButtonAddFacultyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddFacultyActionPerformed
        try {
            SimpleDateFormat dFormat = new SimpleDateFormat("yyyy-MM-dd");
            String checkQuery = "SELECT * FROM Faculty WHERE Faculty_ID = ?";

            String FacultyID = jTextFieldFacultyID.getText();
            String FName = jTextFieldFName.getText();
            String MName = jTextFieldMName.getText();
            String SName = jTextFieldSName.getText();
            String Bdate = dFormat.format(jDateChooserBdate.getDate());
            jDateChooserBdate.setDate(new Date());
            String Age = jTextFieldAge.getText();
            String Address = jTextFieldAddress.getText();
            String PhoneNo = jTextFieldPhoneNo.getText();
            String Gender = jComboBoxGender.getSelectedItem().toString();
            String DeptID = jComboBoxDeptID.getSelectedItem().toString();

            if (FacultyID.isEmpty() || MName.isEmpty() || FName.isEmpty() || SName.isEmpty() || Bdate.isEmpty() || Age.isEmpty() || Address.isEmpty() || PhoneNo.isEmpty() || Gender.isEmpty() || DeptID.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Pls enter all fields.", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                Connection con = DBConnection.getConnection();
                PreparedStatement ps = con.prepareStatement(checkQuery);
                ps.setString(1, FacultyID);
                ResultSet rs = ps.executeQuery();

                try {
                    theQuery("INSERT INTO Faculty(Faculty_ID, First_Name, Middle_Name, Surname, Birth_Date, Age, Address, Phone_No, Gender, Department_ID)"
                            + "VALUES(" + FacultyID + ", '"
                            + FName + "', '"
                            + MName + "', '"
                            + SName + "', '"
                            + Bdate + "', "
                            + Age + ", '"
                            + Address + "', '"
                            + PhoneNo + "', '"
                            + Gender + "', '"
                            + DeptID + "')"
                    );
                    updateJTable(jTable1, tModel, columnsName, rowData);
                    jTabbedPane1.setSelectedIndex(0);
                    JOptionPane.showMessageDialog(null, "Successfully Added.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex, "Check your details.", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Enter all fields.", "ERROR", JOptionPane.ERROR_MESSAGE);
        } finally {
            jTextFieldFacultyID.setText("");
            jTextFieldFName.setText("");
            jTextFieldMName.setText("");
            jTextFieldSName.setText("");
            jDateChooserBdate.setDate(null);
            jTextFieldAge.setText("");
            jTextFieldAddress.setText("");
            jTextFieldPhoneNo.setText("");
            jComboBoxGender.setSelectedIndex(0);
            jComboBoxDeptID.setSelectedIndex(0);
        }
    }//GEN-LAST:event_jButtonAddFacultyActionPerformed

