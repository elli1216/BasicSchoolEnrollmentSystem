    private static ArrayList<Enroll> getEnroll() {
        ArrayList<Enroll> enroll = new ArrayList<Enroll>();
        Connection con = DBConnection.getConnection();
        Enroll e;
        int selectedRow = jTable1.getSelectedRow();
        String studNo = jTable1.getModel().getValueAt(selectedRow, 0).toString();

        try {

            PreparedStatement ps = con.prepareStatement("""
                                 SELECT Subjects.Subject_ID, Subjects.Subject_Name, Students.Student_ID
                                 FROM Subjects
                                 INNER JOIN Enroll ON Subjects.Subject_ID = Enroll.Subject_ID
                                 INNER JOIN Students ON Enroll.Student_ID = Students.Student_ID
                                 WHERE Students.Student_ID = ?;""");
            ps.setString(1, studNo);
            ResultSet rs = ps.executeQuery(); 
            while (rs.next()) {
                e = new Enroll(
                        rs.getInt("Student_ID"),
                        rs.getString("Subject_ID"),
                        rs.getString("Subject_Name")
                );
                enroll.add(e);
            }
        } catch (SQLException ex) {
            Logger.getLogger(CourseManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
        return enroll;
    }
    
